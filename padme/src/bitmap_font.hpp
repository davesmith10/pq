// ── Text rendering ────────────────────────────────────────────────────────────

// Returns glyph width of text in pixels (counts glyphs, not raw bytes).
// Handles the UTF-8 two-byte © sequence as one glyph.
static unsigned text_pixel_width(const std::string& text) {
    unsigned count = 0;
    for (size_t i = 0; i < text.size(); ) {
        unsigned char c = (unsigned char)text[i];
        if (c == 0xC2 && i + 1 < text.size() &&
            (unsigned char)text[i + 1] == 0xA9) {
            ++count; i += 2;
        } else {
            ++count; ++i;
        }
    }
    return count * FONT_W;
}

// Render text into an RGBA pixel buffer at position (x, y).
// fg and bg are {R,G,B} arrays. Alpha is always 255.
// Handles the UTF-8 ©  sequence (0xC2 0xA9) as a single glyph.
static void draw_text(std::vector<uint8_t>& pixels, unsigned img_w,
                      unsigned x, unsigned y,
                      const std::string& text,
                      std::array<uint8_t, 3> fg,
                      std::array<uint8_t, 3> bg)
{
    unsigned cx = x;
    for (size_t i = 0; i < text.size(); ) {
        unsigned char c = (unsigned char)text[i];
        const uint8_t* glyph;

        if (c == 0xC2 && i + 1 < text.size() &&
            (unsigned char)text[i + 1] == 0xA9) {
            glyph = FONT5x8[95];  // ©
            i += 2;
        } else if (c >= 0x20 && c <= 0x7E) {
            glyph = FONT5x8[c - 0x20];
            ++i;
        } else {
            glyph = FONT5x8[0];  // space for unknown / control
            ++i;
        }

        for (unsigned row = 0; row < FONT_H; ++row) {
            for (unsigned col = 0; col < 5; ++col) {
                bool on = (glyph[row] >> (4 - col)) & 1;
                auto& color = on ? fg : bg;
                size_t px = ((y + row) * img_w + (cx + col)) * 4;
                pixels[px + 0] = color[0];
                pixels[px + 1] = color[1];
                pixels[px + 2] = color[2];
                pixels[px + 3] = 255;
            }
        }
        cx += FONT_W;
    }
}
